package com.ssquare.java.strings.string_class;

public class Conversion_Method {
    public static void main(String[] args){
        String lowercase = "siddik";
        String uppercase = "AMIT";

        System.out.println("String Lower Case is :"+uppercase.toLowerCase());
        System.out.println("String Upper Case is :"+lowercase.toUpperCase());
    }
}

/*
Output of above Program will be:
String Lower Case is :amit
String Upper Case is :SIDDIK
 */