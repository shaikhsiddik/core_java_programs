package com.ssquare.java.data_type.primitivedata_type;

public class PrimitiveDataType {
    public static void main(String[] args) {
        /*
        Numeric
        1) Integer
        2) Floating Point
        */
        int c = 10;
        short o = 15;
        long d = 123456789;
        byte e = 1;
        float k = 98.5f;
        double u = 12536.5;
        System.out.println("Integer Value of C--->"+c);
        System.out.println("Short Value of O--->"+o);
        System.out.println("Long Value of D--->"+d);
        System.out.println("Byte Value of E--->"+e);
        System.out.println("Float Value of K--->"+k);
        System.out.println("Double Value of U--->"+u);
        /*
        Non-Numeric
         */
        char l = 'A';
        boolean p = true;
        System.out.println("Char Value of L--->"+l);
        System.out.println("Boolean Value of P--->"+p);
    }
}

